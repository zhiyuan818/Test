package com.jjxt.ssm.utils;

public class LogParameter {
	private String key;
	private String flag;
	private Integer amount;
	private String param;
	private String result;
	private String srcChannelId;
	private String destChannelId;
	private Integer appId;
	private Integer priority;

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getFlag() {
		return flag;
	}

	public void setFlag(String flag) {
		this.flag = flag;
	}

	public Integer getAmount() {
		return amount;
	}

	public void setAmount(Integer amount) {
		this.amount = amount;
	}

	public String getParam() {
		return param;
	}

	public void setParam(String param) {
		this.param = param;
	}

	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}

	public String getSrcChannelId() {
		return srcChannelId;
	}

	public void setSrcChannelId(String srcChannelId) {
		this.srcChannelId = srcChannelId;
	}

	public String getDestChannelId() {
		return destChannelId;
	}

	public void setDestChannelId(String destChannelId) {
		this.destChannelId = destChannelId;
	}

	public Integer getAppId() {
		return appId;
	}

	public void setAppId(Integer appId) {
		this.appId = appId;
	}

	public Integer getPriority() {
		return priority;
	}

	public void setPriority(Integer priority) {
		this.priority = priority;
	}

}
